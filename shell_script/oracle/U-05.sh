#!/bin/bash
# shell_script/oracle/U-05.sh
# -----------------------------------------------------------------------------
# [U-05] PATH 환경변수 설정 (Oracle Linux)
# -----------------------------------------------------------------------------
# - 관련 법령: ISMS-P 2.6.1(시스템 하드닝)
# - 목적: PATH 환경변수에 '.'(현재 디렉터리)이 포함되어 발생하는 명령어 위조 공격 방어
# -----------------------------------------------------------------------------

set -u

CODE="U-05"
CATEGORY="계정 관리"
RISK="상"
ITEM="PATH 환경변수 설정"

RESULT="양호"
STATUS=""

if echo "$PATH" | grep -qE "\.:|::|:\."; then
    RESULT="취약"
    STATUS="PATH 환경변수에 '.' 또는 빈 경로가 포함되어 있어 우선순위 위조 위험이 있습니다."
else
    STATUS="PATH 환경변수 설정이 적절합니다."
fi

if [[ "$RESULT" == "양호" ]]; then
    STATUS="[양호] $STATUS"
else
    STATUS="[취약] $STATUS"
fi

# ==== 표준 출력 (Markdown) ====
cat << __MD_EOF__
# ${CODE}: ${ITEM}

| 항목 | 내용 |
|------|------|
| 분류 | ${CATEGORY} |
| 코드 | ${CODE} |
| 위험도 | ${RISK} |
| 진단항목 | ${ITEM} |
| 진단결과 | **${RESULT}** |
| 현황 | ${STATUS} |
| 대응방안 | PATH 환경변수에서 '.' 을 제거하거나 맨 뒤로 이동 |

__MD_EOF__
